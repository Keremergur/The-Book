
public class Loops {
    public static void main(String[] args) {

        boolean condition = false;
        
        while(condition){
            // ...
        }

        do {
            // at least once
        } while (condition); //semicolon

        for(int i = 0; i < 10; i++){
            // ...
        }

        double[] list = {2.15, 6.42, 3.87};

        for(double item : list){
            // ...
        }
    }
}

